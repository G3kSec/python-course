# Arithmetic Operators - Operadores Aitméticos
# Addition - Suma (+)
addition = 2 + 2
# Subtraction - Resta (-)
subtraction = 4 - 2
# Multiplication - Multiplicación (*)
multiplication = 2 * 3
# Division - División (/)
division = 10 / 5
# Exponentiation - Exponenciación (**)
exponentiation = 15 ** 5
# Floor division - División baja (//)
floor_division = 12 // 6
# Modulus - Módulo o Resto (%)
modulus = 18 % 6